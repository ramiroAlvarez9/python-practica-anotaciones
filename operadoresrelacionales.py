# el operador relacional establece una relacion entre dos elementos, dandole un valor de verdad o falsedad. Las relaciones que podemos establecer, son:

# == igual
# != Distintos
# < menor
# > mayor
# <= menor o igual
# >= mayor o igual

a = 1
b = 2

